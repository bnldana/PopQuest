<h1>Politique de cookies</h1>
<p>Popcorn Quest utilise des cookies et des technologies similaires pour garantir le meilleur fonctionnement possible de notre plateforme et pour améliorer votre expérience en ligne. Cette politique explique comment et pourquoi nous utilisons ces technologies et les choix dont vous disposez.</p>

<h2>Qu'est-ce qu'un cookie ?</h2>
<p>Un cookie est un petit fichier texte stocké sur votre ordinateur ou appareil mobile lorsque vous visitez un site web. Il permet au site de se souvenir de vos actions et préférences (telles que la connexion, la langue, la taille des caractères et d'autres préférences d'affichage) sur une période donnée, donc vous n'avez pas à les saisir chaque fois que vous revenez sur le site ou naviguez d'une page à une autre.</p>

<h2>Comment utilisons-nous les cookies ?</h2>
<p>Nous utilisons des cookies pour :
<p>
<ul>
    <li>Assurer le fonctionnement efficace de Popcorn Quest.</li>
    <li>Analyser notre trafic à l'aide de Google Analytics dans le respect du Google Consent Mode version 2. Cela signifie que Google Analytics ne collectera des données qu'avec votre consentement et respectera vos choix en matière de confidentialité.</li>
    <li>Mémoriser vos préférences de consentement et fournir des expériences personnalisées.</li>
</ul>

<h2>Types de cookies utilisés</h2>
<ul>
    <li>Cookies essentiels : Nécessaires au fonctionnement technique du site. Ils vous permettent de naviguer sur Popcorn Quest et d'utiliser ses fonctionnalités.</li>
    <li>Cookies analytiques : Collectent des informations sur votre utilisation du site et nous permettent d'améliorer son fonctionnement.</li>
    <li>Cookies de fonctionnalité : Servent à reconnaître lorsque vous revenez sur notre site et permettent de vous offrir des fonctionnalités améliorées et plus personnelles.</li>
</ul>

<h2>Gestion de vos préférences de cookies</h2>
<p>À votre première visite sur Popcorn Quest, un bandeau de consentement s'affichera, vous demandant de choisir les catégories de cookies que vous acceptez d'utiliser. Vous pouvez modifier vos préférences à tout moment en accédant à notre outil de gestion des cookies.</p>

<h2>Google Consent Mode version 2</h2>
<p>Nous avons adopté Google Consent Mode version 2, qui ajuste la manière dont les services de Google utilisent les cookies et collectent des données en fonction de votre consentement. Cela assure que vos préférences en matière de confidentialité sont respectées sans compromettre l'analyse du trafic web, permettant une meilleure compréhension de l'utilisation du site tout en protégeant votre vie privée.</p>

<h2>Vos droits</h2>
<p>Vous avez le droit de choisir si oui ou non vous acceptez l'utilisation de cookies non essentiels. Néanmoins, bloquer certains types de cookies peut affecter votre expérience sur le site et les services que nous sommes en mesure d'offrir.</p>

<p>Cette politique de cookies est susceptible d’être mise à jour. Nous vous encourageons à vérifier cette page régulièrement pour vous tenir informé des changements.</p>
