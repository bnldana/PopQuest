<h1>Politique de cookies</h1>
<p>Popcorn Quest utilise des cookies pour garantir le bon fonctionnement de notre plateforme et améliorer votre expérience de jeu. Cette politique explique comment et pourquoi nous utilisons ces cookies.</p>

<h2>Qu'est-ce qu'un cookie ?</h2>
<p>Un cookie est un petit fichier texte stocké sur votre ordinateur ou appareil mobile lorsque vous visitez un site web. Il permet au site de se souvenir de vos actions et préférences sur une période donnée, comme vos scores et votre pseudonyme.</p>

<h2>Comment utilisons-nous les cookies ?</h2>
<p>Nous utilisons des cookies pour :
<ul>
    <li>Assurer le fonctionnement efficace de Popcorn Quest.</li>
    <li>Enregistrer vos scores et votre pseudonyme pour une durée d'un an afin de personnaliser votre expérience et vous permettre de suivre vos progrès dans le jeu.</li>
</ul>

<h2>Types de cookies utilisés</h2>
<ul>
    <li>Cookies essentiels : Nécessaires au fonctionnement technique du site. Ils vous permettent de naviguer sur Popcorn Quest et d'utiliser ses fonctionnalités.</li>
    <li>Cookies de jeu : Enregistrent vos scores et votre pseudonyme pour personnaliser votre expérience de jeu.</li>
</ul>

<h2>Gestion de vos préférences de cookies</h2>
<p>À votre première visite sur Popcorn Quest, un bandeau de consentement s'affichera, vous permettant de choisir d'accepter ou non l'utilisation des cookies.</p>

<h2>Vos droits</h2>
<p>Vous avez le droit de choisir si oui ou non vous acceptez l'utilisation des cookies non essentiels. Cependant, bloquer certains types de cookies peut affecter votre expérience sur le site et les services que nous sommes en mesure d'offrir.</p>

<p>Cette politique de cookies est susceptible d’être mise à jour. Nous vous encourageons à vérifier cette page régulièrement pour vous tenir informé des changements.</p>
