<h1>Politique de confidentialité</h1>
<p>Popcorn Quest accorde une grande importance à la protection de la vie privée et des données personnelles. Cette politique vise à informer les utilisateurs sur la manière dont leurs données sont collectées, traitées, et protégées.</p>

<h2>Responsable du traitement des données</h2>
<p>Popcorn Quest, situé à Strasbourg, est responsable du traitement des données personnelles collectées sur le site.</p>

<h2>Collecte des données</h2>
<p>Les données personnelles collectées peuvent inclure : pseudo, adresse e-mail, et données d'usage du site (scores, niveaux atteints). Elles sont collectées via des formulaires et lors de votre navigation sur le site (cookies).</p>

<h2>Finalité du traitement</h2>
<p>Vos données sont traitées dans le but d'améliorer votre expérience sur le site, de gérer votre compte utilisateur, d'envoyer des newsletters, et de réaliser des statistiques d'usage.</p>

<h2>Base légale</h2>
<p>Le traitement de vos données est fondé sur votre consentement, recueilli explicitement via des formulaires en ligne.</p>

<h2>Droits des utilisateurs</h2>
<p>Conformément au RGPD, vous disposez des droits d'accès, de rectification, de suppression, de limitation, d'opposition au traitement de vos données personnelles, ainsi que du droit à la portabilité de ces données. Vous pouvez exercer ces droits en contactant Popcorn Quest à hello@popcornquest.fun.</p>

<h2>Sécurité des données</h2>
<p>Popcorn Quest met en œuvre des mesures techniques et organisationnelles pour assurer la sécurité de vos données personnelles contre toute forme de traitement non autorisé ou illégal.</p>
